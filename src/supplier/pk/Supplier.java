    protected void dataTable() {
        Object[] Baris = {
            "ID",
            "Nama",
            "No.Telp",
            "Alamat"
        };
        
        tabMode = new DefaultTableModel(null, Baris);
        tableSupplier.setModel(tabMode);
        String query = "SELECT * FROM supplier WHERE nama LIKE'%"+ searchTxt.getText() +"%' ORDER BY nama ASC";
        
        try{
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(query);
            
            while(rs.next()) {
                String id = rs.getString("id");
                String nama = rs.getString("nama");
                String noTelp = rs.getString("no_telp");
                String alamat = rs.getString("alamat");
                
                String[] data = { id, nama, noTelp, alamat };
                tabMode.addRow(data);
            }
        } catch (Exception e) {
            System.out.println("ERROR GET LIST DATA" + e);
        }
    }

