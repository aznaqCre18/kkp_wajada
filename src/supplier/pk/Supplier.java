    private void tableSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSupplierMouseClicked
        // TODO add your handling code here:
        int bar = tableSupplier.getSelectedRow();
        saveBtn.setEnabled(false);
        
        String id = tabMode.getValueAt(bar, 0).toString();
        String nama = tabMode.getValueAt(bar, 1).toString();
        String noTelp = tabMode.getValueAt(bar, 2).toString();
        String alamat = tabMode.getValueAt(bar, 3).toString();
        
        idTxt.setText(id);
        namaTxt.setText(nama);
        noTelpTxt.setText(noTelp);
        alamatTxt.setText(alamat);
    }//GEN-LAST:event_tableSupplierMouseClicked

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String query = "INSERT INTO supplier VALUES (?, ?, ?, ?, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP)";
        try {
            PreparedStatement stat = conn.prepareStatement(query);
            
            stat.setString(1, null);
            stat.setString(2, namaTxt.getText());
            stat.setString(3, alamatTxt.getText());
            stat.setString(4, noTelpTxt.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
            kosong();
            namaTxt.requestFocus();
            dataTable();
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Database error : " + e.getMessage());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

