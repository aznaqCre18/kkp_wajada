    protected void dataTable() {
        Object[] Baris = {
            "User Kode",
            "Nama",
            "Username",
            "Password"
        };
        
        tabMode = new DefaultTableModel(null, Baris);
        tableUser.setModel(tabMode);
        String query = "SELECT * FROM user WHERE nama LIKE'%"+ searchTxt.getText() +"%' ORDER BY nama ASC";
        
        try{
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(query);
            
            while(rs.next()) {
                String user_kode = rs.getString("user_code");
                String nama = rs.getString("nama");
                String username = rs.getString("username");
                String password = rs.getString("password");
                
                String[] data = { user_kode, nama, username, password };
                tabMode.addRow(data);
            }
        } catch (Exception e) {
            System.out.println("ERROR GET LIST DATA" + e);
        }
    }

