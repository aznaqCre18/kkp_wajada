    private void tableKaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableKaryawanMouseClicked
        // TODO add your handling code here:
        int bar = tableKaryawan.getSelectedRow();
        btnSave.setEnabled(false);
        
        String id = tabMode.getValueAt(bar, 0).toString();
        String nama = tabMode.getValueAt(bar, 1).toString();
        String jenisKelamin = tabMode.getValueAt(bar, 2).toString();
        String noTelp = tabMode.getValueAt(bar, 3).toString();
        String alamat = tabMode.getValueAt(bar, 4).toString();
        
        idTxt.setText(id);
        namaTxt.setText(nama);
        jenisKelaminTxt.setSelectedItem(jenisKelamin);
        noTelpTxt.setText(noTelp);
        alamatTxt.setText(alamat);
    }//GEN-LAST:event_tableKaryawanMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String query = "INSERT INTO karyawan VALUES (?, ?, ?, ?, ?, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP)";
        try {
            PreparedStatement stat = conn.prepareStatement(query);
            
            stat.setString(1, null);
            stat.setString(2, jenisKelaminTxt.getSelectedItem().toString());
            stat.setString(3, namaTxt.getText());
            stat.setString(4, noTelpTxt.getText());
            stat.setString(5, alamatTxt.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
            kosong();
            namaTxt.requestFocus();
            dataTable();
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Database error : " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

